def resolve_sub_skill(skill: str, sub_skill: str | None) -> tuple[str | None, str]:
    """归一化子能力 id 并取预设文本。

    返回 (sub_id, preset_text)：sub_skill 缺省或未知时返回 (None, "")，
    调用方按 v1 行为处理（不注入、run 记录不带后缀）。
    """
    if not sub_skill:
        return None, ""
    table = SUB_SKILL_PRESETS.get(skill, {})
    if sub_skill not in table:
        return None, ""
    return sub_skill, table[sub_skill]


def skill_label(skill: str, sub_id: str | None) -> str:
    """runs 表的 skill 字段值：带子能力时记 "exam:quiz" 复合串."""
    return f"{skill}:{sub_id}" if sub_id else skill
